        /*
        To represent real numbers, C# uses the following number types: float, double and decimal.

        float: ±1.5 x 10−45 to ±3.4 x 1038 » ~6-9 digits » 4 bytes
        double: ±5.0 × 10−324 to ±1.7 × 10308 » ~15-17 digits » 8 bytes
        decimal: ±1.0 x 10-28 to ±7.9228 x 1028 » 28-29 digits » 16 bytes

        Float variables must be end, in the asignment part, the prefix f or F at the end:
            float rate = 5.2F;
            float amount = 10_000f;

        Double variables does not have prefix:
            double dimension = 3.14;
            double radius = 1_000.5;

        We can add the prefix d or D anyway:
            double dimension = 3.14D;
            double radius = 1_000.5d;

        Decimal numbers has the m and M prefix:
            decimal amount = 9.99m;
            decimal tax = 0.08M;
         */


    }
